a = int(input("masukkan bilangan pertama: "))
b = int(input("masukkan bilangan kedua: "))

print("hasil operasi bitwise and:", a & b)
print("hasil operasi bitwise or:", a | b)
print("hasil operasi bitwise xor:", a ^ b)
print("hasil operasi bitwise not bilangan pertama:", ~a)
print("hasil operasi bitwise not bilangan kedua:", ~b)