# input panjang dan lebar sbg string
panjangStr = input("Masukkan panjang persegi panjang: ")
lebarStr = input("Masukkan lebar persegi panjang: ")

# konversi panjang dan lebar ke float
panjang = float(panjangStr)
lebar = float (lebarStr)

# hitung luas persegi panjang
luas = panjang * lebar

# cetak hasil
print("Luas persi panjang: ", luas)